//Question 2:  Write a program to print an integer and a float value

#include <stdio.h>

int main() {
    int intValue = 42; //integer value declaration plus initialization
    float floatValue = 3.14159; //float value declaration plus initialization

    printf("Integer value: %d\n", intValue);
    printf("Float value: %.2f\n", floatValue);

    return 0;
}

